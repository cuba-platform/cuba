package com.haulmont.cuba.core.app;

import org.dom4j.Document;

/**
 * Implement this interface if you want to modify {@code orm.xml} generated by the framework.
 * <p>
 * Implementation class must be registered in the {@code cuba.ormXmlPostProcessor} app property.
 */
public interface OrmXmlPostProcessor {

    /**
     * Invoked by the framework after generation of {@code orm.xml}.
     * @param ormXml content of {@code orm.xml} as a Dom4j document
     */
    void process(Document ormXml);
}
